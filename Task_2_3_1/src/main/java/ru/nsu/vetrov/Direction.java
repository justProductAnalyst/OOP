package ru.nsu.vetrov;

/**
 * Enumerates the possible directions in which a snake can move in the game.
 */
public enum Direction {
    /** Represents movement in the upward direction. */
    UP,

    /** Represents movement in the downward direction. */
    DOWN,

    /** Represents movement to the left. */
    LEFT,

    /** Represents movement to the right. */
    RIGHT
}
